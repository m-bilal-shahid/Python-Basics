# Comparison Operators (also called Relational Operators).
# These operators compare two values and always return True or False
# They are super useful in conditions (like if, while, etc.)


# 1. == (Equal to) 
# → Returns True if both values are equal
# print(5 == 5)   

# # 2. != (Not equal to)
# #  → Returns True if values are not equal
# print(5 != 3)   

# # 3. > (Greater than) 
# # → Returns True if left value is greater
# print(10 > 5)   

# # 4. < (Less than) 
# # → Returns True if left value is smaller
# print(3 < 7)    

# # 5. >= (Greater than or equal to) 
# # → Returns True if left value is greater or equal
# print(5 >= 5)   

# # 6. <= (Less than or equal to) 
# # → Returns True if left value is smaller or equal
# print(9 <= 6)   





# Example:

a=int(input("Enter First Number: "))
b=int(input("Enter Second Number: "))


print("Equal To:", a==b)
print("Not Equal To:",a!=b)
print("Greater Than:",a>b)
print("Less Than:", a<b)
print("Greater Than or Equal:",a>=b)
print("Less Than or Equal:", a<=b)