# Arithmetic Operators in Python:

#1. + (Addition):
# # It Adds two or more numbers.
# print(5 + 3)   


# #2. - (Subtraction):
# #  Subtracts the right number from the left.

# print(5 - 3)   


# #3.  * (Multiplication):
# # Multiplies two numbers.

# print(5 * 3)   


# #4.  / (Division):
# #  Divides left number by right â†’ result is always float.

