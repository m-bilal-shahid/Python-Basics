# print(5 // 2)  


# #6. % (Modulus):
# #  Returns the remainder of division.

# print(5 % 2)   


# #7. ** (Exponent / Power):
# #  Raises a number to the power of another.

# print(2 ** 3) 



# Example:

a=int(input("Enter First Number: "))
